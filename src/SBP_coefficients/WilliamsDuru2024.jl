"""
    WilliamsDuru2024(version::Symbol)

Coefficients of the upwind SBP operators given in
- Williams, Duru (2024)
  Full-Spectrum Dispersion Relation Preserving Summation-By-Parts Operators.
  SIAM Journal on Numerical Analysis 62.4, pp. 1565-1588.

You can choose between the different versions `:central`, `:plus`, and `:minus`.
"""
struct WilliamsDuru2024 <: SourceOfCoefficients
    kind::Symbol

    function WilliamsDuru2024(kind::Symbol)
        if (kind !== :plus) && (kind !== :minus) && (kind !== :central)
            throw(
                ArgumentError(
                    "The only choices are :plus, :minus, and :central, not :$kind.",
                ),
            )
        end
        new(kind)
    end
end

function Base.show(io::IO, source::WilliamsDuru2024)
    if get(io, :compact, false)
        summary(io, source)
    else
        print(
            io,
            "Williams, Duru (2024) \n",
            "  Full-Spectrum Dispersion Relation Preserving Summation-By-Parts Operators. \n",
            "  SIAM Journal on Numerical Analysis 62.4, pp. 1565-1588. \n",
            "  (upwind coefficients ",
            source.kind,
            ")",
        )
    end
end


